<?php

namespace Modules\Post\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use Modules\Post\Models\Post;

class SendMailToUserAfterSubmitPostForm extends Mailable
{
    use Queueable, SerializesModels;

    public $post;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct(Post $post)
    {
        $this->post = $post;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->from('null@studio.io', 'null studio')
                    ->subject("Null Studio: Submit Post was successfully")
                    ->view('post::mail.submit-post-form');
    }
}
